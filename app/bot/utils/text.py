"""Утилиты для безопасной работы с текстом в Telegram-сообщениях.

Telegram ограничивает одно сообщение 4096 символами. Если вставить в сообщение
переменный по длине текст (ответ ребёнка, контекст смены, текст отчёта) без
ограничения, Telegram вернёт ошибку `MESSAGE_TOO_LONG` и кнопка/действие
«залипнет».

Здесь собраны хелперы, которые делают такой вывод безопасным:
- truncate_for_telegram — обрезает длинный текст с пометкой (для предпросмотров,
  где полный текст всё равно доступен отдельно — редактирование/просмотр);
- split_text — разбивает длинный текст на части (когда нужно показать целиком
  несколькими сообщениями).
"""

# Жёсткий лимит Telegram на длину одного сообщения.
TG_MESSAGE_LIMIT = 4096

# Безопасный лимит с запасом под окружающую разметку (заголовки, blockquote,
# подсказки и т.п.), которая добавляется к переменному тексту.
TG_SAFE_LIMIT = 3500


def truncate_for_telegram(text: str, limit: int = 2000) -> str:
    """Обрезает текст до limit символов, добавляя пометку об обрезке.

    Используется для ПРЕДПРОСМОТРОВ переменного по длине текста, который
    вставляется в одно сообщение вместе с другой разметкой. Полный текст при
    этом остаётся доступен пользователю через отдельные экраны (просмотр/правка).
    """
    if text is None:
        return ""
    text = str(text)
    if len(text) <= limit:
        return text
    return text[:limit].rstrip() + f"\n\n…(текст обрезан, всего {len(text)} символов)"


def split_text(text: str, max_len: int = TG_SAFE_LIMIT) -> list[str]:
    """Разбивает длинный текст на части для отправки несколькими сообщениями.

    Старается резать по переносу строки, чтобы не рвать слова/абзацы.
    """
    if not text:
        return [""]
    if len(text) <= max_len:
        return [text]
    parts: list[str] = []
    while text:
        if len(text) <= max_len:
            parts.append(text)
            break
        split_at = text.rfind("\n", 0, max_len)
        if split_at == -1:
            split_at = max_len
        parts.append(text[:split_at])
        text = text[split_at:].lstrip("\n")
    return parts
