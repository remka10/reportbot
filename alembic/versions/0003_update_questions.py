def _apply(questions: list[tuple], deactivate_from: int) -> None:
    questions_t = sa.table(
        "questions",
        sa.column("block_number", sa.Integer),
        sa.column("block_title", sa.String),
        sa.column("question_number", sa.Integer),
        sa.column("question_text", sa.Text),
        sa.column("is_active", sa.Boolean),
    )
    conn = op.get_bind()
    for bn, bt, qn, qt in questions:
        conn.execute(
            questions_t.update()
            .where(questions_t.c.question_number == qn)
            .values(block_number=bn, block_title=bt, question_text=qt, is_active=True)
        )
    # Деактивируем вопросы с номерами >= deactivate_from (их нет в новом наборе).
    conn.execute(
        questions_t.update()
        .where(questions_t.c.question_number >= deactivate_from)
        .values(is_active=False)
    )


